-- Migration 1: 20260526181245 - Base Tables
-- ============================================================
CREATE TABLE IF NOT EXISTS user_roles (
  id BIGINT PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
  user_id UUID NOT NULL REFERENCES auth.users (id),
  role TEXT NOT NULL,
  created_at TIMESTAMP DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS profiles (
  id UUID PRIMARY KEY REFERENCES auth.users (id) ON DELETE CASCADE,
  username TEXT UNIQUE,
  full_name TEXT,
  phone TEXT,
  avatar_url TEXT,
  bio TEXT,
  wins INT DEFAULT 0,
  losses INT DEFAULT 0,
  rating DECIMAL DEFAULT 1000,
  updated_at TIMESTAMP DEFAULT NOW()
);

-- ============================================================
-- Migration 2: 20260526181257 - Tournaments
-- ============================================================
CREATE TABLE IF NOT EXISTS tournaments (
  id UUID PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name TEXT NOT NULL,
  description TEXT,
  status TEXT DEFAULT 'pending',
  prize_amount DECIMAL,
  max_players INT,
  start_date TIMESTAMP,
  end_date TIMESTAMP,
  created_by UUID REFERENCES auth.users (id),
  created_at TIMESTAMP DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS tournament_participants (
  id UUID PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  tournament_id UUID REFERENCES tournaments (id) ON DELETE CASCADE,
  user_id UUID REFERENCES auth.users (id) ON DELETE CASCADE,
  status TEXT DEFAULT 'active',
  joined_at TIMESTAMP DEFAULT NOW()
);

-- ============================================================
-- Migration 3: 20260601102444 - Live Matches & Signaling
-- ============================================================
CREATE TABLE IF NOT EXISTS live_matches (
  id UUID PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  host_id UUID NOT NULL REFERENCES auth.users (id),
  join_code TEXT UNIQUE NOT NULL,
  home_player TEXT,
  away_player TEXT,
  home_score INT DEFAULT 0,
  away_score INT DEFAULT 0,
  status TEXT DEFAULT 'live',
  started_at TIMESTAMP DEFAULT NOW(),
  ended_at TIMESTAMP,
  recording_url TEXT,
  created_at TIMESTAMP DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS live_signaling (
  id UUID PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  match_id UUID REFERENCES live_matches (id) ON DELETE CASCADE,
  from_user UUID REFERENCES auth.users (id),
  to_user UUID REFERENCES auth.users (id),
  kind TEXT,
  payload JSONB,
  created_at TIMESTAMP DEFAULT NOW()
);

-- ============================================================
-- Migration 4: 20260601103140 - Live Chat
-- ============================================================
CREATE TABLE IF NOT EXISTS live_chat (
  id UUID PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  match_id UUID REFERENCES live_matches (id) ON DELETE CASCADE,
  user_id UUID REFERENCES auth.users (id),
  username TEXT NOT NULL,
  body TEXT NOT NULL,
  created_at TIMESTAMP DEFAULT NOW()
);

-- ============================================================
-- Migration 5: 20260601103202 - Direct Messages
-- ============================================================
CREATE TABLE IF NOT EXISTS dm_threads (
  id UUID PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  user_a_id UUID NOT NULL REFERENCES auth.users (id),
  user_b_id UUID NOT NULL REFERENCES auth.users (id),
  created_at TIMESTAMP DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS dm_messages (
  id UUID PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  thread_id UUID REFERENCES dm_threads (id) ON DELETE CASCADE,
  user_id UUID REFERENCES auth.users (id),
  body TEXT NOT NULL,
  created_at TIMESTAMP DEFAULT NOW()
);

