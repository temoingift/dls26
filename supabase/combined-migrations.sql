-- Profiles policies
CREATE POLICY "Profiles are viewable by everyone" ON profiles
  FOR SELECT USING (true);

CREATE POLICY "Users can update own profile" ON profiles
  FOR UPDATE USING (auth.uid() = id);

-- Tournaments policies
CREATE POLICY "Tournaments are viewable by everyone" ON tournaments
  FOR SELECT USING (true);

CREATE POLICY "Users can create tournaments" ON tournaments
  FOR INSERT WITH CHECK (auth.uid() = created_by);

CREATE POLICY "Tournament creators can update their tournaments" ON tournaments
  FOR UPDATE USING (auth.uid() = created_by);

-- Live matches policies
CREATE POLICY "Live matches are viewable by everyone" ON live_matches
  FOR SELECT USING (true);

CREATE POLICY "Users can create live matches" ON live_matches
  FOR INSERT WITH CHECK (auth.uid() = host_id);

CREATE POLICY "Match hosts can update their matches" ON live_matches
  FOR UPDATE USING (auth.uid() = host_id);

-- Live signaling policies
CREATE POLICY "Users can view signaling for their matches" ON live_signaling
  FOR SELECT USING (
    auth.uid() = from_user OR 
    auth.uid() = to_user OR
    auth.uid() IN (
      SELECT host_id FROM live_matches WHERE id = match_id
    )
  );

CREATE POLICY "Users can insert signaling messages" ON live_signaling
  FOR INSERT WITH CHECK (auth.uid() = from_user);

-- Live chat policies
CREATE POLICY "Live chat is viewable by everyone" ON live_chat
  FOR SELECT USING (true);

CREATE POLICY "Users can insert chat messages" ON live_chat
  FOR INSERT WITH CHECK (auth.uid() = user_id);

-- Direct messages policies
CREATE POLICY "Users can view their DM threads" ON dm_threads
  FOR SELECT USING (auth.uid() = user_a_id OR auth.uid() = user_b_id);

CREATE POLICY "Users can view their DM messages" ON dm_messages
  FOR SELECT USING (
    auth.uid() IN (
      SELECT user_a_id FROM dm_threads WHERE id = thread_id
      UNION
      SELECT user_b_id FROM dm_threads WHERE id = thread_id
    )
  );

CREATE POLICY "Users can insert DM messages" ON dm_messages
  FOR INSERT WITH CHECK (
    auth.uid() IN (
      SELECT user_a_id FROM dm_threads WHERE id = thread_id
      UNION
      SELECT user_b_id FROM dm_threads WHERE id = thread_id
    )
  );

